# Creación de variables
a = 2 + 6 # retorna un entero
b = 50 - 5*6 # retorna un entero
c = (50 - 5*6)/4
d = 8/5 # la división siempre retorna un flotante
# Impresión de resultados
print("2 + 6 = ", a) # Muestra la suma de 2 más 6
print("50 - 5*6 = ", b) # Muestra 50  menos el producto de 5x6
print("(50 - 5*6)/4 = ", c) 
print("8 / 5 = ", d) 
e = 17 // 3
f = 17 % 3
print("17 // 3 =", e) # Imprime el cociente de la división
print("17 % 3 =", f) # Imprime el resto de la división
g = 5 ** 2
print("5 ** 2 = ", g) # Muestra el cuadrado de 5

width = 20
height = 5 * 9
print("width * height = ", width * height)
